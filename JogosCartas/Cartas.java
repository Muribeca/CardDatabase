package JogosCartas;

public class Cartas {
    private String NomeCarta,NomeColecao,NumeroColecao,QuantidadeCarta,TipoCarta;

    public String getNomeCarta() {
        return NomeCarta;
    }

    public void setNomeCarta(String nomeCarta) {
        NomeCarta = nomeCarta;
    }

    public String getNomeColecao() {
        return NomeColecao;
    }

    public void setNomeColecao(String nomeColecao) {
        NomeColecao = nomeColecao;
    }

    public String getNumeroColecao() {
        return NumeroColecao;
    }

    public void setNumeroColecao(String numeroColecao) {
        NumeroColecao = numeroColecao;
    }

    public String getQuantidadeCarta() {
        return QuantidadeCarta;
    }

    public void setQuantidadeCarta(String quantidadeCarta) {
        QuantidadeCarta = quantidadeCarta;
    }

    public String getTipoCarta() {
        return TipoCarta;
    }

    public void setTipoCarta(String tipoCarta) {
        TipoCarta = tipoCarta;
    }
}
